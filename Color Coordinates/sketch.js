
let r = 255;
let g = 255;
let b = 255;


function setup() {
  createCanvas(400, 400);

}

function draw() {
  background(0);


  if (mouseX,mouseY) {
      r = (255,140,100);
      g = (255);
      b = (255);


if (mouseX < 400 & mouseY < 400){
rect(0,0,200,200);
fill(r,g,b, mouseX,mouseY);
}
if (mouseX < 400 & mouseY < 400){
rect(200,0,200,200);
fill(b, mouseX,mouseY);
}

if (mouseX < 400 & mouseY < 400){
rect(0,200,200,200);
fill(r, mouseX,mouseY);
}
if (mouseX < 400 & mouseY < 400){
rect(200,200,200,200);
fill(g, mouseX,mouseY);
}

  }

}
